/*
when we want to do somthing more than one time we use loops to do that.
we use loop, to avoid repetition
*/
#include <iostream>
using namespace std;
int main()
{
    // Print 10 times good morning
    for (int i = 1; i <= 10; i++)
    {
        cout << "Good Morning" << endl;
    }
    return 0;
}